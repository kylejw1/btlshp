        public static void GetTemplateDimensions(string template, out int width, out int height)
        {
            // For simplicity, assume all characters except newline have a width of 1.  
            // Tabs will be replaced when defined in the templates
            var split = (template ?? "").Split(new string[] { Environment.NewLine }, StringSplitOptions.None);
            width = split.Max(s => s.Length);
            height = split.Length;
        }

